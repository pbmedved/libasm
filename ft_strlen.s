GLOBAL _ft_strlen

_ft_strlen:
	mov		rax,0
	cmp		BYTE[rdi + rax],0
	jg		_while
	ret
_while:
	inc		rax
	cmp		BYTE[rdi + rax],0
	jg		_while
	ret